// Number type conversion
console.log("12" / "3"); //auto conversion happens

// Explicit conversion
let string_2 = "6";
let string_1 = "3";
console.log(typeof string_1,typeof string_2); //string
console.log(string_2 / string_1) //Implicit conversion
console.log(Number(string_2) / Number(string_1)) //Explicit conversion